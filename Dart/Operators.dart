void main() {
  //Arthmetic operator
  int a = 10, b = 20;

  int add = a + b;
  print(add);
  int sub = a - b;
  print(sub);
  int mul = a * b;
  print(mul);
  int divisible = a ~/ b; //use ~ symbol
  print(divisible);
  double div = a / b;
  print(div);
  int moduls = a % b;
  print(moduls);
}
